function results = run_priority_saturation_experiment(options)
%run_priority_saturation_experiment launches the recommended next experiment.
%
% This entrypoint keeps the current valid baseline fixed:
%   - observationVariant = "markov52"
%   - TD3 feedforward
%   - rewardActionWeight = 0.01
%   - rewardDeltaActionWeight = 0.05
% and only sweeps the explicit saturation penalty.

arguments
    options = struct()
end

options = normalizePrioritySaturationOptions(options);

results = run_markov52_saturation_reward_sweep(struct( ...
    "rewardActionWeight", options.rewardActionWeight, ...
    "rewardDeltaActionWeight", options.rewardDeltaActionWeight, ...
    "saturationThresholds", options.saturationThresholds, ...
    "saturationWeights", options.saturationWeights, ...
    "trainingEpisodes", options.trainingEpisodes, ...
    "trainingSaveEvery", options.trainingSaveEvery, ...
    "trainingPlots", options.trainingPlots, ...
    "auditFastSimulations", options.auditFastSimulations, ...
    "auditFullSimulations", options.auditFullSimulations, ...
    "auditTopK", options.auditTopK, ...
    "auditEveryK", options.auditEveryK, ...
    "auditTailCount", options.auditTailCount, ...
    "resultsRoot", options.resultsRoot, ...
    "enableDetailedActionDiagnostics", options.enableDetailedActionDiagnostics, ...
    "savePerMotorMetrics", options.savePerMotorMetrics));
end

function options = normalizePrioritySaturationOptions(options)
defaults = struct( ...
    "rewardActionWeight", 0.01, ...
    "rewardDeltaActionWeight", 0.05, ...
    "saturationThresholds", [0.90], ...
    "saturationWeights", [0.01 0.02 0.05], ...
    "trainingEpisodes", 3000, ...
    "trainingSaveEvery", 100, ...
    "trainingPlots", "training-progress", ...
    "auditFastSimulations", 10, ...
    "auditFullSimulations", 30, ...
    "auditTopK", 1, ...
    "auditEveryK", 100, ...
    "auditTailCount", 8, ...
    "resultsRoot", "", ...
    "enableDetailedActionDiagnostics", false, ...
    "savePerMotorMetrics", true);

fields = fieldnames(defaults);
for i = 1:numel(fields)
    if ~isfield(options, fields{i}) || isempty(options.(fields{i}))
        options.(fields{i}) = defaults.(fields{i});
    end
end
end
