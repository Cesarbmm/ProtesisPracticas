    summary.trackingMaeCi95Low, summary.trackingMaeCi95High);
lines(end+1) = sprintf("actionL2 = %.6f +- %.6f", ...
    summary.actionL2Mean, summary.actionL2Std);
lines(end+1) = sprintf("actionL2 95%% CI = [%.6f, %.6f]", ...
    summary.actionL2Ci95Low, summary.actionL2Ci95High);
