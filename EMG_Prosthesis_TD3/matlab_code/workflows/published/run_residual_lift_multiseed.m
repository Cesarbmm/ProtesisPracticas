lines(end+1) = strtrim(string(evalc("disp(results.perSeedTable)")));
textValue = strjoin(lines, newline);
end

function resultsRoot = resolveMultiseedResultsRoot(requestedRoot, repoRoot)
requestedRoot = string(requestedRoot);
if strlength(requestedRoot) > 0
    resultsRoot = char(requestedRoot);
else
    resultsRoot = fullfile( ...
        repoRoot, "Agentes", "residual_lift_multiseed", ...
        string(datetime("now", "Format", "yy-MM-dd HH mm ss")));
end
end

function writeTextFile(filePath, textValue)
fid = fopen(filePath, "w");
if fid < 0
    error("Could not open %s for writing.", filePath);
end
cleanup = onCleanup(@() fclose(fid)); %#ok<NASGU>
fprintf(fid, "%s", textValue);
end

function stats = computeSummaryStats(values)
values = double(values(:));
values = values(~isnan(values));
n = numel(values);

if n == 0
    stats = struct("mean", NaN, "std", NaN, "ci95Half", NaN, ...
        "ci95Low", NaN, "ci95High", NaN, "n", 0);
    return;
end

mu = mean(values);
sigma = std(values, 0);
if n <= 1
    ci95Half = NaN;
else
    ci95Half = confidenceHalfWidth95(sigma, n);
end

stats = struct( ...
    "mean", mu, ...
    "std", sigma, ...
    "ci95Half", ci95Half, ...
    "ci95Low", mu - ci95Half, ...
    "ci95High", mu + ci95Half, ...
    "n", n);
end

function halfWidth = confidenceHalfWidth95(sigma, n)
if n <= 1 || isnan(sigma)
    halfWidth = NaN;
    return;
end

if exist("tinv", "file") == 2
    tCritical = tinv(0.975, n - 1);
else
    tCritical = approximateTCritical95(n - 1);
end
halfWidth = tCritical * sigma / sqrt(n);
end

function tCritical = approximateTCritical95(df)
switch df
    case 1
        tCritical = 12.706;
    case 2
        tCritical = 4.303;
    case 3
        tCritical = 3.182;
    case 4
        tCritical = 2.776;
    case 5
        tCritical = 2.571;
    case 6
        tCritical = 2.447;
    case 7
        tCritical = 2.365;
    case 8
        tCritical = 2.306;
    case 9
        tCritical = 2.262;
    case 10
        tCritical = 2.228;
    otherwise
        tCritical = 1.96;
end
end

function strongClaimSupported = evaluateStrongClaim(summary, benchmark)
acceptedCount = summary.conditionACount + summary.conditionBCount;
trackingWithinMargin = summary.trackingMseMean <= benchmark.trackingMse * 1.03;
effortImproved = summary.actionL2Mean < benchmark.actionL2;
saturationImproved = summary.saturationFractionMean < benchmark.saturationFraction;
strongClaimSupported = acceptedCount >= 3 && trackingWithinMargin && ...
    effortImproved && saturationImproved;
end
