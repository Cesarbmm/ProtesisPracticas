function results = run_residual_lift_multiseed(options)
%run_residual_lift_multiseed runs the Residual Lift pipeline across seeds.

arguments
    options = struct()
end

options = normalizeMultiseedOptions(options);

paths = resolveMatlabCodePaths(string(mfilename("fullpath")));
matlabRoot = char(paths.matlabRoot);
workspaceRoot = char(paths.workspaceRoot);
cd(matlabRoot);
addpath(genpath(fullfile(matlabRoot, "src")));
addpath(genpath(fullfile(matlabRoot, "config")));
addpath(genpath(fullfile(matlabRoot, "lib")));
addpath(genpath(fullfile(matlabRoot, "agents")));

resultsRoot = resolveMultiseedResultsRoot(options.resultsRoot, workspaceRoot);
ensureDirectoryExists(resultsRoot);

seedResults = cell(numel(options.seeds), 1);
seedRows = cell(numel(options.seeds), 1);

for i = 1:numel(options.seeds)
    seedValue = double(options.seeds(i));
    seedLabel = sprintf("seed_%03d", round(seedValue));
    seedRoot = fullfile(resultsRoot, char(seedLabel));
    ensureDirectoryExists(seedRoot);

    pilotOptions = struct( ...
        "baseCheckpointPath", options.baseCheckpointPath, ...
        "baseLabel", options.baseLabel, ...
        "residualScale", options.residualScale, ...
        "residualHiddenUnits", options.residualHiddenUnits, ...
        "explorationStd", options.explorationStd, ...
        "explorationStdMin", options.explorationStdMin, ...
        "explorationStdDecayRate", options.explorationStdDecayRate, ...
        "resetExperienceBufferBeforeTraining", options.resetExperienceBufferBeforeTraining, ...
        "trainingEpisodes", options.trainingEpisodes, ...
        "trainingSaveEvery", options.trainingSaveEvery, ...
        "trainingPlots", options.trainingPlots, ...
        "randomSeed", seedValue, ...
        "enableDetailedActionDiagnostics", options.enableDetailedActionDiagnostics, ...
        "savePerMotorMetrics", options.savePerMotorMetrics, ...
        "auditFastSimulations", options.auditFastSimulations, ...
        "auditFullSimulations", options.auditFullSimulations, ...
        "auditTopK", options.auditTopK, ...
        "auditEveryK", options.auditEveryK, ...
        "auditTailCount", options.auditTailCount, ...
        "visualTestSimulations", options.visualTestSimulations, ...
        "alwaysRunVisualTest", true, ...
        "pretestTrackingMseMax", options.pretestTrackingMseMax, ...
        "pretestSaturationFractionMax", options.pretestSaturationFractionMax, ...
        "pretestActionL2Max", options.pretestActionL2Max, ...
        "resultsRoot", string(seedRoot));

    pilotResults = run_residual_lift_pilot(pilotOptions);
    seedResults{i, 1} = pilotResults;
    seedRows{i, 1} = buildSeedRow(pilotResults, seedValue);
end

perSeedTable = struct2table(vertcat(seedRows{:}));
aggregateSummary = buildAggregateSummary(perSeedTable);

results = struct();
results.resultsRoot = string(resultsRoot);
results.benchmark = getAgent7250Benchmark();
results.options = options;
results.perSeedTable = perSeedTable;
results.aggregateSummary = aggregateSummary;
results.seedResults = seedResults;

writetable(perSeedTable, fullfile(resultsRoot, "residual_lift_multiseed_summary.csv"));
save(fullfile(resultsRoot, "residual_lift_multiseed_results.mat"), ...
    "results", "perSeedTable", "aggregateSummary", "seedResults", "options");
writeTextFile(fullfile(resultsRoot, "residual_lift_multiseed_summary.txt"), ...
    buildMultiseedSummaryText(results));

figurePath = fullfile(resultsRoot, "residual_lift_multiseed_summary.png");
createMultiseedSummaryFigure(perSeedTable, results.benchmark, figurePath);
results.summaryFigurePath = string(figurePath);

save(fullfile(resultsRoot, "residual_lift_multiseed_results.mat"), ...
    "results", "perSeedTable", "aggregateSummary", "seedResults", "options");
end

function options = normalizeMultiseedOptions(options)
defaults = struct( ...
    "baseCheckpointPath", getAgent7250CheckpointPath(), ...
    "baseLabel", "Agent7250", ...
    "residualScale", 0.20, ...
    "residualHiddenUnits", 32, ...
    "explorationStd", 0.02, ...
    "explorationStdMin", 0.002, ...
    "explorationStdDecayRate", 1e-4, ...
    "resetExperienceBufferBeforeTraining", false, ...
    "trainingEpisodes", 2000, ...
    "trainingSaveEvery", 50, ...
    "trainingPlots", "none", ...
    "randomSeeds", [11 22 33 44 55], ...
    "enableDetailedActionDiagnostics", false, ...
    "savePerMotorMetrics", true, ...
    "auditFastSimulations", 20, ...
    "auditFullSimulations", 50, ...
    "auditTopK", 2, ...
    "auditEveryK", 50, ...
    "auditTailCount", 12, ...
    "visualTestSimulations", 50, ...
    "pretestTrackingMseMax", 0.044022, ...
    "pretestSaturationFractionMax", 0.35271, ...
    "pretestActionL2Max", 0.57794, ...
    "resultsRoot", "");

fields = fieldnames(defaults);
for i = 1:numel(fields)
    if ~isfield(options, fields{i}) || isempty(options.(fields{i}))
        options.(fields{i}) = defaults.(fields{i});
    end
end

if isfield(options, "seeds") && ~isempty(options.seeds)
    options.randomSeeds = options.seeds;
end

options.baseCheckpointPath = string(options.baseCheckpointPath);
options.baseLabel = string(options.baseLabel);
options.trainingPlots = string(options.trainingPlots);
options.seeds = double(options.randomSeeds(:))';
end

function seedRow = buildSeedRow(pilotResults, seedValue)
row = table2struct(pilotResults.consolidatedTable(1, :));

finalTrackingMse = selectMetric(row, "visualTrackingMseMean", "trackingMseMean");
finalTrackingMae = selectMetric(row, "visualTrackingMaeMean", "trackingMaeMean");
finalActionL2 = selectMetric(row, "visualActionL2Mean", "actionL2Mean");
finalSaturationFraction = selectMetric(row, "visualSaturationFractionMean", "saturationFractionMean");
finalDeltaActionL2 = selectMetric(row, "visualDeltaActionL2Mean", "deltaActionL2Mean");

finalStatus = string(row.benchmarkStatus);
if isfield(row, "visualBenchmarkStatus") && strlength(strtrim(string(row.visualBenchmarkStatus))) > 0
    finalStatus = string(row.visualBenchmarkStatus);
end

seedRow = struct( ...
    "seed", double(seedValue), ...
    "trainingRunDir", string(row.trainingRunDir), ...
    "bestCheckpointPath", string(row.bestCheckpointPath), ...
    "bestCheckpointEpisode", double(row.bestCheckpointEpisode), ...
    "visualTestExecuted", logical(row.visualTestExecuted), ...
    "finalStatus", finalStatus, ...
    "finalTrackingMse", finalTrackingMse, ...
    "finalTrackingMae", finalTrackingMae, ...
    "finalActionL2", finalActionL2, ...
    "finalSaturationFraction", finalSaturationFraction, ...
    "finalDeltaActionL2", finalDeltaActionL2, ...
    "auditTrackingMse", double(row.trackingMseMean), ...
    "auditActionL2", double(row.actionL2Mean), ...
    "auditSaturationFraction", double(row.saturationFractionMean), ...
    "auditDeltaActionL2", double(row.deltaActionL2Mean), ...
    "residualActionL2Mean", selectMetric(row, "residualActionL2Mean", "residualActionL2Mean"), ...
    "baseToFinalActionDeltaMean", selectMetric(row, "baseToFinalActionDeltaMean", "baseToFinalActionDeltaMean"), ...
    "signOverrideFractionMean", selectMetric(row, "signOverrideFractionMean", "signOverrideFractionMean"), ...
    "meetsConditionA", finalStatus == "ConditionA", ...
    "meetsConditionB", finalStatus == "ConditionB");
end

function value = selectMetric(row, preferredField, fallbackField)
value = NaN;
if isfield(row, preferredField)
    preferredValue = double(row.(preferredField));
    if ~isnan(preferredValue)
        value = preferredValue;
        return;
    end
end
if isfield(row, fallbackField)
    value = double(row.(fallbackField));
end
end

function aggregateSummary = buildAggregateSummary(perSeedTable)
statuses = string(perSeedTable.finalStatus);
trackingStats = computeSummaryStats(perSeedTable.finalTrackingMse);
trackingMaeStats = computeSummaryStats(perSeedTable.finalTrackingMae);
actionStats = computeSummaryStats(perSeedTable.finalActionL2);
saturationStats = computeSummaryStats(perSeedTable.finalSaturationFraction);
deltaStats = computeSummaryStats(perSeedTable.finalDeltaActionL2);
benchmark = getAgent7250Benchmark();

aggregateSummary = struct( ...
    "numSeeds", height(perSeedTable), ...
    "trackingMseMean", trackingStats.mean, ...
    "trackingMseStd", trackingStats.std, ...
    "trackingMseCi95Half", trackingStats.ci95Half, ...
    "trackingMseCi95Low", trackingStats.ci95Low, ...
    "trackingMseCi95High", trackingStats.ci95High, ...
    "trackingMaeMean", trackingMaeStats.mean, ...
    "trackingMaeStd", trackingMaeStats.std, ...
    "trackingMaeCi95Half", trackingMaeStats.ci95Half, ...
    "trackingMaeCi95Low", trackingMaeStats.ci95Low, ...
    "trackingMaeCi95High", trackingMaeStats.ci95High, ...
    "actionL2Mean", actionStats.mean, ...
    "actionL2Std", actionStats.std, ...
    "actionL2Ci95Half", actionStats.ci95Half, ...
    "actionL2Ci95Low", actionStats.ci95Low, ...
    "actionL2Ci95High", actionStats.ci95High, ...
    "saturationFractionMean", saturationStats.mean, ...
    "saturationFractionStd", saturationStats.std, ...
    "saturationFractionCi95Half", saturationStats.ci95Half, ...
    "saturationFractionCi95Low", saturationStats.ci95Low, ...
    "saturationFractionCi95High", saturationStats.ci95High, ...
    "deltaActionL2Mean", deltaStats.mean, ...
    "deltaActionL2Std", deltaStats.std, ...
    "deltaActionL2Ci95Half", deltaStats.ci95Half, ...
    "deltaActionL2Ci95Low", deltaStats.ci95Low, ...
    "deltaActionL2Ci95High", deltaStats.ci95High, ...
    "conditionACount", sum(statuses == "ConditionA"), ...
    "conditionBCount", sum(statuses == "ConditionB"), ...
    "rejectedCount", sum(statuses == "Rejected"));

[~, bestIdx] = min(perSeedTable.finalTrackingMse, [], "omitnan");
[~, worstIdx] = max(perSeedTable.finalTrackingMse, [], "omitnan");
aggregateSummary.bestSeed = double(perSeedTable.seed(bestIdx));
aggregateSummary.worstSeed = double(perSeedTable.seed(worstIdx));
aggregateSummary.bestCheckpointPath = string(perSeedTable.bestCheckpointPath(bestIdx));
aggregateSummary.worstCheckpointPath = string(perSeedTable.bestCheckpointPath(worstIdx));
aggregateSummary.strongClaimSupported = evaluateStrongClaim(aggregateSummary, benchmark);
end

function createMultiseedSummaryFigure(perSeedTable, benchmark, figurePath)
metricLabels = {'trackingMSE', 'actionL2', 'saturationFraction', 'deltaActionL2'};
means = [ ...
    mean(perSeedTable.finalTrackingMse, "omitnan"), ...
    mean(perSeedTable.finalActionL2, "omitnan"), ...
    mean(perSeedTable.finalSaturationFraction, "omitnan"), ...
    mean(perSeedTable.finalDeltaActionL2, "omitnan")];
ci95Half = [ ...
    computeSummaryStats(perSeedTable.finalTrackingMse).ci95Half, ...
    computeSummaryStats(perSeedTable.finalActionL2).ci95Half, ...
    computeSummaryStats(perSeedTable.finalSaturationFraction).ci95Half, ...
    computeSummaryStats(perSeedTable.finalDeltaActionL2).ci95Half];
benchmarkValues = [ ...
    benchmark.trackingMse, ...
    benchmark.actionL2, ...
    benchmark.saturationFraction, ...
    benchmark.deltaActionL2];
perSeedValues = { ...
    perSeedTable.finalTrackingMse, ...
    perSeedTable.finalActionL2, ...
    perSeedTable.finalSaturationFraction, ...
    perSeedTable.finalDeltaActionL2};

f = figure("Visible", "off", "Color", "w", "Position", [100 100 1300 700]);
t = tiledlayout(f, 2, 2, "TileSpacing", "compact", "Padding", "compact");
for i = 1:numel(metricLabels)
    nexttile(t);
    bar(1, means(i), "FaceColor", [0.13 0.47 0.70], "BarWidth", 0.55);
    hold on
    errorbar(1, means(i), ci95Half(i), "k", "LineWidth", 1.6, "CapSize", 10);
    yline(benchmarkValues(i), "--", "Color", [0.80 0.10 0.10], "LineWidth", 1.6);
    scatter(ones(height(perSeedTable), 1), perSeedValues{i}, 42, ...
        "filled", "MarkerFaceColor", [0.90 0.60 0.00], "MarkerFaceAlpha", 0.85);
    hold off
    xlim([0.5 1.5]);
    xticks(1);
    xticklabels({'Residual multi-seed'});
    ylabel(metricLabels{i});
    title(metricLabels{i}, "FontWeight", "bold");
    grid on
    legend({"mean", "95% CI", "Agent7250", "per-seed"}, ...
        "Location", "best", "Box", "off");
end
title(t, "Residual Lift reproducibility summary (mean and 95% CI over seeds)", ...
    "FontWeight", "bold");
exportgraphics(f, figurePath, "Resolution", 220);
close(f);
end

