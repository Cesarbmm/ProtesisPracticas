function trainingInfo = runCheckpointTest(checkpointPath, numSimulations, plotEpisodes, options)
%runCheckpointTest evaluates a saved checkpoint in simulation mode.

arguments
    checkpointPath (1, 1) string = defaultCheckpointPath()
    numSimulations (1, 1) double {mustBeInteger, mustBePositive} = 50
    plotEpisodes (1, 1) logical = true
    options = struct()
end

paths = resolveMatlabCodePaths(string(mfilename("fullpath")));
matlabRoot = char(paths.matlabRoot);
cd(matlabRoot);

addpath(genpath(fullfile(matlabRoot, "src")));
addpath(genpath(fullfile(matlabRoot, "config")));
addpath(genpath(fullfile(matlabRoot, "lib")));
addpath(genpath(fullfile(matlabRoot, "agents")));

override = loadCheckpointOverride(checkpointPath);
agentId = "td3";
if isfield(override, "agent_id") && strlength(string(override.agent_id)) > 0
    agentId = string(override.agent_id);
end
override = mergeStructs(override, struct( ...
    "run_training", false, ...
    "newTraining", false, ...
    "agentFile", checkpointPath, ...
    "agent_id", agentId, ...
    "plotEpisodeOnTest", plotEpisodes, ...
    "flagSaveTraining", true, ...
    "episode_save_freq", 1, ...
    "verbose", false, ...
    "usePrerecorded", true, ...
    "simMotors", true, ...
    "connect_glove", false, ...
    "simOpts", rlSimulationOptions( ...
        'MaxSteps', 500, ...
        'NumSimulations', numSimulations, ...
        'StopOnError', 'on', ...
        'UseParallel', false)));

if isfield(options, "resultsRoot") && strlength(string(options.resultsRoot)) > 0
    resultsRoot = char(string(options.resultsRoot));
    override.agents_directory = @(agent_id, variant) fullfile( ...
        resultsRoot, string(datetime("now", "Format", "yy-MM-dd HH m s")));
end

if isfield(options, "overridePatch") && isstruct(options.overridePatch)
    override = mergeStructs(override, options.overridePatch);
end

setConfigurablesOverride(override);
clear configurables
cleanup = onCleanup(@() clearConfigurablesOverride()); %#ok<NASGU>

trainingInfo = trainInterface("td3", "", "");
end

function checkpointPath = defaultCheckpointPath()
checkpointPath = getResidualFinalCheckpointPath();
end

function override = loadCheckpointOverride(checkpointPath)
override = struct();
configPath = fullfile(fileparts(checkpointPath), "00_configs.mat");
if ~isfile(configPath)
    return;
end

aux = load(configPath, "configs");
if ~isfield(aux, "configs")
    return;
end

savedConfigs = aux.configs;
overrideFields = [ ...
    "stateLength", ...
    "rewardType", ...
    "rewardActionWeight", ...
    "rewardProgressWeight", ...
    "rewardSmoothnessWeight", ...
    "rewardDeltaActionWeight", ...
    "rewardSaturationWeight", ...
    "rewardSaturationThreshold", ...
    "unifyActions", ...
    "speeds", ...
    "quantizeCommandsForSimulation", ...
    "actionInterfaceVariant", ...
    "actionCommandActivationThreshold", ...
    "actionCommandLevels", ...
    "actionWarpDeadzone", ...
    "actionWarpOutputLevels", ...
    "rf_modify_actions", ...
    "usePrerecorded", ...
    "simMotors", ...
    "connect_glove", ...
    "dataset", ...
    "dataset_folder", ...
    "numEMGFeatures", ...
    "observationVariant", ...
    "emgHistoryLength", ...
    "encodersLimits", ...
    "EMGFeaturesLimits", ...
    "enableDetailedActionDiagnostics", ...
    "savePerMotorMetrics", ...
    "td3Residual", ...
    "agent_id" ...
    ];

for i = 1:numel(overrideFields)
    fieldName = overrideFields(i);
    if isfield(savedConfigs, fieldName)
        override.(fieldName) = savedConfigs.(fieldName);
    end
end

if isfield(override, "td3Residual")
    override.td3Residual = normalizeResidualConfig(override.td3Residual);
end
end

function merged = mergeStructs(baseStruct, patchStruct)
merged = baseStruct;
fields = fieldnames(patchStruct);
for i = 1:numel(fields)
    merged.(fields{i}) = patchStruct.(fields{i});
end
end

function clearConfigurablesOverride()
if isappdata(0, 'configurables_override')
    rmappdata(0, 'configurables_override');
end
if isappdata(0, 'configurables_override_key')
    rmappdata(0, 'configurables_override_key');
end
clear configurables
end

function td3Residual = normalizeResidualConfig(td3Residual)
if ~isstruct(td3Residual)
    return;
end
if ~isfield(td3Residual, "baseCheckpointPath") || ...
        strlength(string(td3Residual.baseCheckpointPath)) == 0 || ...
        ~isfile(string(td3Residual.baseCheckpointPath))
    td3Residual.baseCheckpointPath = getAgent7250CheckpointPath();
end
end
